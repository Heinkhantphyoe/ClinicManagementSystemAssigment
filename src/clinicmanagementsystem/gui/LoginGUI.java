    private User[] loadUsers() throws IOException {
        String[] lines = FileManager.readLines(USERS_FILE);
        User[] users = new User[lines.length];

        for (int i = 0; i < lines.length; i++) {
            String line = lines[i];
            if (line == null || line.trim().isEmpty()) {
                continue;
            }

            String[] parts = line.split(",", -1);
            if (parts.length < 8) {
                String[] padded = new String[8];
                for(int j=0; j<8; j++) padded[j] = (j < parts.length) ? parts[j] : "";
                parts = padded;
            }

            users[i] = new User(
                    parts[0].trim(),
                    parts[1].trim(),
                    parts[2].trim(),
                    parts[3].trim(),
                    parts[4].trim(),
                    parts[5].trim(),
                    parts[6].trim(),
                    parts[7].trim()
            );
        }

        return users;
    }

    private void openDashboard(String role) {
        JFrame dashboard;

        switch (role.toLowerCase()) {
            case "admin":
                dashboard = new AdminDashboardGUI();
                break;
            case "doctor":
                dashboard = new DoctorDashboardGUI();
                break;
            case "nurse":
                dashboard = new NurseDashboardGUI();
                break;
            case "receptionist":
                dashboard = new ReceptionistDashboardGUI();
                break;
            default:
                JOptionPane.showMessageDialog(this, "Unsupported user role: " + role);
                return;
        }

        dashboard.setVisible(true);
        dispose();
    }

