    private User login(String username, String password, User[] users) {
        for (int i = 0; i < users.length; i++) {
            User user = users[i];
            if (user != null && user.getUsername().equals(username) && user.getPassword().equals(password)) {
                return user;
            }
        }
        return null;
    }
    
