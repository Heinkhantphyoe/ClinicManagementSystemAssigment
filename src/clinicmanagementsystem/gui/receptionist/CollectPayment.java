    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if (!validateInput()) {
            return;
        }

        String paymentID = txtPaymentID.getText().trim();
        
        // Prevent duplicate Payment IDs
        try (BufferedReader br = new BufferedReader(new FileReader("src/clinicmanagementsystem/data/payments.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                if (line.trim().isEmpty()) continue;
                String[] data = line.split(",");
                if (data[0].equalsIgnoreCase(paymentID)) {
                    JOptionPane.showMessageDialog(this, "Payment ID already exists.");
                    return;
                }
            }
        } catch (Exception e) {
            // file might not exist yet
        }

        try {
            BufferedWriter bw = new BufferedWriter(
                    new FileWriter("src/clinicmanagementsystem/data/payments.txt", true)
            );

            String data = txtPaymentID.getText().trim() + ","
                    + txtAppointmentID.getText().trim() + ","
                    + txtPatientID.getText().trim() + ","
                    + txtPatientName.getText().trim() + ","
                    + txtDate.getText().trim() + ","
                    + txtAmount.getText().trim() + ","
                    + cmbPaymentMethod.getSelectedItem().toString() + ","
                    + cmbPaymentStatus.getSelectedItem().toString();

            bw.write(data);
            bw.newLine();
            bw.close();

            JOptionPane.showMessageDialog(this, "Payment collected successfully!");

            loadPayments();
            clearFields();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnaddActionPerformed

