    private boolean validateInput() {
        if (txtPaymentID.getText().trim().isEmpty() ||
            txtAppointmentID.getText().trim().isEmpty() ||
            txtPatientID.getText().trim().isEmpty() ||
            txtPatientName.getText().trim().isEmpty() ||
            txtDate.getText().trim().isEmpty() ||
            txtAmount.getText().trim().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "All fields are required.");
            return false;
        }

        if (!txtDate.getText().trim().matches("^\\d{4}-\\d{2}-\\d{2}$")) {
            JOptionPane.showMessageDialog(this, "Date must be in YYYY-MM-DD format.");
            return false;
        }

        try {
            double amount = Double.parseDouble(txtAmount.getText().trim());
            if (amount < 0) {
                JOptionPane.showMessageDialog(this, "Amount cannot be negative.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Amount must be a valid number.");
            return false;
        }

        return true;
    }

    private void loadPayments() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            BufferedReader br = new BufferedReader(
                    new FileReader("src/clinicmanagementsystem/data/payments.txt")
            );

            String line;

            while ((line = br.readLine()) != null) {
                if (line.toLowerCase().startsWith("paymentid")) {
                    continue;
                }
                if (line.trim().isEmpty()) {
                    continue;
                }

                String[] data = line.split(",", -1);
                String[] paddedData = new String[8];
                for(int i=0; i<8; i++) {
                    paddedData[i] = (i < data.length) ? data[i] : "";
                }
                model.addRow(paddedData);
            }

            br.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

