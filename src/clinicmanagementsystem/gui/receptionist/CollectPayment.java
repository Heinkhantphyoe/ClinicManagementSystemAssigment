    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        if (row == -1 || jTable1.getValueAt(row, 0) == null) {
            return;
        }

        txtPaymentID.setText(jTable1.getValueAt(row, 0) != null ? jTable1.getValueAt(row, 0).toString() : "");
        txtAppointmentID.setText(jTable1.getValueAt(row, 1) != null ? jTable1.getValueAt(row, 1).toString() : "");
        txtPatientID.setText(jTable1.getValueAt(row, 2) != null ? jTable1.getValueAt(row, 2).toString() : "");
        txtPatientName.setText(jTable1.getValueAt(row, 3) != null ? jTable1.getValueAt(row, 3).toString() : "");
        txtDate.setText(jTable1.getValueAt(row, 4) != null ? jTable1.getValueAt(row, 4).toString() : "");
        txtAmount.setText(jTable1.getValueAt(row, 5) != null ? jTable1.getValueAt(row, 5).toString() : "");
        
        Object methodObj = jTable1.getValueAt(row, 6);
        if (methodObj != null && !methodObj.toString().isEmpty()) cmbPaymentMethod.setSelectedItem(methodObj.toString());
        
        Object statusObj = jTable1.getValueAt(row, 7);
        if (statusObj != null && !statusObj.toString().isEmpty()) cmbPaymentStatus.setSelectedItem(statusObj.toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        String paymentID = txtPaymentID.getText().trim();
        if (paymentID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a Payment to delete.");
            return;
        }

        try {
            File inputFile = new File("src/clinicmanagementsystem/data/payments.txt");
            File tempFile = new File("src/clinicmanagementsystem/data/temp.txt");

            Scanner scanner = new Scanner(inputFile);
            PrintWriter writer = new PrintWriter(tempFile);
            boolean found = false;

            while (scanner.hasNextLine()) {
                String line = scanner.nextLine();
                if (line.trim().isEmpty()) continue;

                if (line.startsWith(paymentID + ",")) {
                    found = true;
                } else {
                    writer.println(line);
                }
            }

            scanner.close();
            writer.close();

            if (found) {
                inputFile.delete();
                tempFile.renameTo(inputFile);
                JOptionPane.showMessageDialog(this, "Payment deleted successfully!");
                loadPayments();
                clearFields();
            } else {
                tempFile.delete();
                JOptionPane.showMessageDialog(this, "Payment ID not found.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        String searchID = txtPaymentID.getText().trim();
        if (searchID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Payment ID to search.");
            loadPayments();
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            BufferedReader br = new BufferedReader(
                    new FileReader("src/clinicmanagementsystem/data/payments.txt")
            );

            String line;
            boolean found = false;

            while ((line = br.readLine()) != null) {
                if (line.toLowerCase().startsWith("paymentid")) continue;
                if (line.trim().isEmpty()) continue;

                String[] data = line.split(",", -1);

                if (data[0].equalsIgnoreCase(searchID)) {
                    String[] paddedData = new String[8];
                    for(int i=0; i<8; i++) {
                        paddedData[i] = (i < data.length) ? data[i] : "";
                    }
                    model.addRow(paddedData);
                    
                    txtPaymentID.setText(paddedData[0]);
                    txtAppointmentID.setText(paddedData[1]);
                    txtPatientID.setText(paddedData[2]);
                    txtPatientName.setText(paddedData[3]);
                    txtDate.setText(paddedData[4]);
                    txtAmount.setText(paddedData[5]);
                    if (!paddedData[6].isEmpty()) cmbPaymentMethod.setSelectedItem(paddedData[6]);
                    if (!paddedData[7].isEmpty()) cmbPaymentStatus.setSelectedItem(paddedData[7]);

                    found = true;
                    break;
                }
            }

            br.close();

            if (!found) {
                JOptionPane.showMessageDialog(this, "Payment not found.");
                loadPayments();
                clearFields();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnsearchActionPerformed

