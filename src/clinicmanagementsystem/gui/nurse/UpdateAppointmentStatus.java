    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String id = txtApptId.getText().trim();
        if (id.isEmpty() || id.equalsIgnoreCase("Enter Appt ID...")) {   // ignore the placeholder
            JOptionPane.showMessageDialog(this, "Enter an Appointment ID to search."); return;
        }
        try {
            String[] lines = FileManager.readLines(APPOINTMENTS_FILE);
            String[] found = null;
            for (String line : lines) {
                if (line == null) continue;
                String[] p = line.split(",");
                // file: apptId,patientId,doctorId,nurseId,date,time,appointType,status,notes
                if (p.length >= 8 && p[0].equalsIgnoreCase(id)) { found = p; break; }
            }
            if (found == null) {
                JOptionPane.showMessageDialog(this, "Appointment not found."); return;
            }
            
            // Hide the misaligned dynamic labels
            lblPatient.setText("");
            lblDate.setText("");
            lblTime.setText("");
            lblDoctor.setText("");
            
            // Append data directly into the neatly stacked title labels to fix visual mismatch
            jLabel1.setText("Patient Name:  " + patientName(found[1]));
            jLabel2.setText("Date:  " + found[4]);
            jLabel3.setText("Time:  " + found[5]);
            jLabel4.setText("Doctor:  " + doctorName(found[2]));
            
            cmbStatus.setSelectedItem(found[7]);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Could not read appointments: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String id = txtApptId.getText().trim();
        if (ValidationUtil.isNullOrEmpty(id) || id.equalsIgnoreCase("Enter Appt ID...")) {
            JOptionPane.showMessageDialog(this, "Search a valid appointment first."); return;
        }
        String newStatus = cmbStatus.getSelectedItem().toString();
        try {
            String[] lines = FileManager.readLines(APPOINTMENTS_FILE);
            boolean found = false;
            for (int i = 0; i < lines.length; i++) {
                if (lines[i] == null) continue;
                String[] p = lines[i].split(",");
                if (p.length >= 8 && p[0].equalsIgnoreCase(id)) {
                    p[7] = newStatus;   // status is the 8th field
                    String notes = (p.length > 8) ? p[8] : "";
                    lines[i] = p[0] + "," + p[1] + "," + p[2] + "," + p[3] + ","
                                 + p[4] + "," + p[5] + "," + p[6] + "," + p[7] + (notes.isEmpty() ? "" : "," + notes);
                    found = true;
                }
            }
            if (found) {
                FileManager.writeLines(APPOINTMENTS_FILE, lines);
                JOptionPane.showMessageDialog(this, "Status updated to " + newStatus);
                txtApptId.setText("Enter Appt ID...");
                jLabel1.setText("Patient Name:");
                jLabel2.setText("Date:");
                jLabel3.setText("Time:");
                jLabel4.setText("Doctor:");
                cmbStatus.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(this, "Appointment not found.");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Could not save status: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

