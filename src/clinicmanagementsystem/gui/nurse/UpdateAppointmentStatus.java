    /** Look up a patient name from patients.txt; falls back to the id. */
    private String patientName(String patientId) {
        try {
            String[] lines = FileManager.readLines(PATIENTS_FILE);
            for (String line : lines) {
                if (line == null) continue;
                String[] p = line.split(",");
                if (p.length >= 2 && p[0].equalsIgnoreCase(patientId)) return p[1];
            }
        } catch (IOException ex) {
            // ignore
        }
        return patientId;
    }

    /** Look up a doctor name from users.txt; falls back to the id. */
    private String doctorName(String doctorId) {
        try {
            String[] lines = FileManager.readLines("src/clinicmanagementsystem/data/users.txt");
            for (String line : lines) {
                if (line == null) continue;
                String[] u = line.split(",");
                if (u.length >= 2 && u[0].equalsIgnoreCase(doctorId)) return u[1];
            }
        } catch (IOException ex) {
            // ignore
        }
        return doctorId;
    }

